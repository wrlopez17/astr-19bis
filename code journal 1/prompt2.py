
# Sum of two floating-point numbers
float_num1 = 5.7
float_num2 = 3.3
sum_floats = float_num1 + float_num2
print("Sum of floating-point numbers:", sum_floats, "Type:", type(sum_floats))

# Difference between two integers
int_num1 = 10
int_num2 = 4
difference_ints = int_num1 - int_num2
print("Difference between integers:", difference_ints, "Type:", type(difference_ints))

# Product of a floating-point number and an integer
product = float_num1 * int_num1
print("Product of a float and an integer:", product, "Type:", type(product))
